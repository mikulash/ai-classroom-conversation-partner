CREATE OR REPLACE FUNCTION public.create_profile_on_signup()
 RETURNS trigger
 LANGUAGE plpgsql
AS $function$
BEGIN
    INSERT INTO public.profiles (user_id, username)
    VALUES (NEW.id, split_part(NEW.email, ''@'', 1));
    RETURN NEW;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.handle_new_user()
 RETURNS trigger
 LANGUAGE plpgsql
 SECURITY DEFINER
 SET search_path TO ''
AS $function$
BEGIN
  INSERT INTO public.profiles (id, full_name, gender, email)
  VALUES (
    new.id,
    new.raw_user_meta_data ->> 'full_name',
    new.raw_user_meta_data ->> 'gender',
    new.email  -- Directly using new.email assuming it's a string
  );
  RETURN new;
END;
$function$
;

CREATE OR REPLACE FUNCTION public.is_admin(user_id uuid)
 RETURNS boolean
 LANGUAGE sql
 STABLE SECURITY DEFINER
AS $function$SELECT
    -- returns true if the user_role for this user_id is 'admin' or 'owner'
    (user_role IN ('admin','owner'))
  FROM public.profiles
  WHERE id = user_id$function$
;

CREATE OR REPLACE FUNCTION public.is_owner(p_user_id uuid)
 RETURNS boolean
 LANGUAGE sql
 STABLE
AS $function$
  SELECT user_role = 'owner'
    FROM public.profiles
   WHERE id = p_user_id;
$function$
;

